package com.exemple.practice.learning;

public class Operators {
    public static void main(String[] args){
        double n = (double)4 / (double)3;
        System.out.println("Printing n before increment: " + n);
        n++;
        System.out.println("Printing n after increment: " + n);
        int a = 6 + 2 * 2;
        int b = 6 + 2 * 2 - 3;
        System.out.println(a + b);
    }
}

